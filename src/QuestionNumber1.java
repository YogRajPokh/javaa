import java.util.Scanner;

public class QuestionNumber1 {
    public static void main(String[] args)
    {
        // Questions number 1
        int firstNumber, secondNumber, sum;
        Scanner sc = new Scanner(System.in);
        System.out.println("Enter first number : ");
        firstNumber = sc.nextInt();
        System.out.println("Enter second number : ");
        secondNumber = sc.nextInt();
        sum = firstNumber + secondNumber;
        System.out.println("The sum of two number is " + sum);

        // Question number 2
        final float pi = 3.1415f;
        float radius, area;
        System.out.println("Enter radius of circle :");
        radius = sc.nextFloat();
        area = pi * radius * radius;
        System.out.println("The area of cirlce is " + area);

        // Question number 3
        int principle, rate, time, si;
        System.out.println("Enter principle, rate and time :");
        principle = sc.nextInt();
        rate = sc.nextInt();
        time = sc.nextInt();
        si = (principle * rate * time)/100;
        System.out.println("The simple interest of amount " + principle + " is " + si);
    }
}
