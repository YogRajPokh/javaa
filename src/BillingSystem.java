import java.util.Scanner;

public class BillingSystem {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        System.out.println("                                           Menu                            ");
        System.out.println("1. Momo     Rs 100                2. Burger     Rs 150                          3. Coffee     Rs 50");
        int selectItems;
        System.out.print("Please Enter food, You want to order : ");
        selectItems = scanner.nextInt();
        System.out.print("Please Enter quantity, You want to order : ");
        int selectQuantity;
        selectQuantity = scanner.nextInt();
        System.out.println("Please Wait !!!");
        System.out.println("Your have ordered successfully");
        System.out.println("                                           Bill                            ");
        int totalAmount = 0;
        if (selectItems == 1){
            totalAmount = 100 * selectQuantity;
        }
        else if(selectItems == 2){
            totalAmount = 150 * selectQuantity;
        }
        else if(selectItems == 3){
            totalAmount = 50 * selectQuantity;
        }
        else{
            System.out.println("Food are not available");
        }
        double afterDiscount = totalAmount - (0.1 * totalAmount);
        double total = afterDiscount + (0.13) * afterDiscount;
        System.out.println("The total price is " + total);
    }
}
