import java.util.Scanner;

public class QuestionNumber4 {
    public static void main(String[] args) {
        float nepaliRupee;
        float usdRupee;
        Scanner sc = new Scanner(System.in);
        System.out.println("Enter nepali rupee : ");
        nepaliRupee = sc.nextFloat();
        usdRupee = nepaliRupee * 132;
        System.out.println("Nepali Rupee " + nepaliRupee + " into usd is " + usdRupee);
    }
}
