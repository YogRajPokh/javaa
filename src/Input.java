import java.util.Scanner;

public class Input {
    public static void main(String[] args) {
        String name;
        int age;
        Scanner sc = new Scanner(System.in);
        System.out.println("Enter your name : ");
        name = sc.next();
        age = sc.nextInt();
        System.out.println(name);
        System.out.println(age);
    }
}
