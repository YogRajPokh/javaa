public class swap {
    public static void main(String[] args) {
        int first = 10;
        int second = 20;
        System.out.println("Initially");
        System.out.println("First " + first);
        System.out.println("Second " + second);
        int store_first = first;
        first = second;
        second = store_first;
        System.out.println("Finally");
        System.out.println("First " + first);
        System.out.println("Second " + second);

    }
}
